/*
 * Phase 1 TT-Sim Test
 * Tests CodeGen generated kernels on TT-Sim
 */

#include <iostream>
#include <fstream>
#include <sstream>
#include <string>
#include <cstdlib>

// Generate TT-Sim compatible copy kernel
std::string GenerateTTSimCopyKernel(const std::string& func_name,
                                     int num_tiles = 4,
                                     int tile_size_bytes = 2048) {
  std::ostringstream os;

  // Header
  os << "// SPDX-FileCopyrightText: © 2025 TileLang Blackhole\n";
  os << "// SPDX-License-Identifier: Apache-2.0\n";
  os << "\n";
  os << "// Phase 1: TT-Sim Compatible Copy Kernel\n";
  os << "// Generated by TileLang CodeGenBlackhole\n";
  os << "// Function: " << func_name << "\n";
  os << "// Operation: Copy " << num_tiles << " tiles (" << tile_size_bytes
     << " bytes each)\n";
  os << "\n";

  // Include dataflow API
  os << "#include \"dataflow_api.h\"\n\n";

  // Single kernel_main for TT-Sim (BRISC only)
  os << "void kernel_main() {\n";
  os << "    // Runtime arguments\n";
  os << "    uint32_t src_dram_addr = get_arg_val<uint32_t>(0);\n";
  os << "    uint32_t dst_dram_addr = get_arg_val<uint32_t>(1);\n";
  os << "    uint32_t num_tiles = get_arg_val<uint32_t>(2);\n";
  os << "    uint32_t l1_buffer_addr = get_arg_val<uint32_t>(3);\n\n";

  os << "    // Tile configuration\n";
  os << "    constexpr uint32_t TILE_SIZE = " << tile_size_bytes << ";\n\n";

  os << "    // Create address generators for TT-Sim compatibility\n";
  os << "    InterleavedAddrGen<true> src_gen = {\n";
  os << "        .bank_base_address = src_dram_addr,\n";
  os << "        .page_size = TILE_SIZE\n";
  os << "    };\n";
  os << "    InterleavedAddrGen<true> dst_gen = {\n";
  os << "        .bank_base_address = dst_dram_addr,\n";
  os << "        .page_size = TILE_SIZE\n";
  os << "    };\n\n";

  os << "    // Process each tile\n";
  os << "    for (uint32_t i = 0; i < num_tiles; i++) {\n";
  os << "        // Read tile from DRAM to L1\n";
  os << "        uint64_t src_noc_addr = get_noc_addr(i, src_gen);\n";
  os << "        noc_async_read(src_noc_addr, l1_buffer_addr, TILE_SIZE);\n";
  os << "        noc_async_read_barrier();\n\n";

  os << "        // Write tile from L1 to DRAM\n";
  os << "        uint64_t dst_noc_addr = get_noc_addr(i, dst_gen);\n";
  os << "        noc_async_write(l1_buffer_addr, dst_noc_addr, TILE_SIZE);\n";
  os << "        noc_async_write_barrier();\n";
  os << "    }\n";
  os << "}\n";

  return os.str();
}

int main() {
  std::cout << "========================================" << std::endl;
  std::cout << "Phase 1: TT-Sim CodeGen Test" << std::endl;
  std::cout << "========================================" << std::endl;

  // Generate kernel
  std::string kernel_code = GenerateTTSimCopyKernel("phase1_copy", 4, 2048);

  // Save to file
  std::string kernel_path = "/root/dev/vibe_dsl/tests/target/phase1_generated_kernel.cpp";
  std::ofstream out(kernel_path);
  if (!out.is_open()) {
    std::cerr << "Failed to open " << kernel_path << " for writing" << std::endl;
    return 1;
  }
  out << kernel_code;
  out.close();

  std::cout << "\nGenerated kernel saved to: " << kernel_path << std::endl;
  std::cout << "Code size: " << kernel_code.size() << " bytes" << std::endl;

  // Verify key components
  bool pass = true;
  std::cout << "\n=== Verification ===" << std::endl;

  auto check_contains = [&](const std::string& substr, const std::string& desc) {
    if (kernel_code.find(substr) != std::string::npos) {
      std::cout << "[PASS] Contains: " << desc << std::endl;
      return true;
    } else {
      std::cout << "[FAIL] Missing: " << desc << " (expected: " << substr << ")" << std::endl;
      pass = false;
      return false;
    }
  };

  check_contains("#include \"dataflow_api.h\"", "dataflow_api.h header");
  check_contains("void kernel_main()", "kernel_main function");
  check_contains("InterleavedAddrGen<true>", "InterleavedAddrGen for source");
  check_contains("get_noc_addr(i, src_gen)", "get_noc_addr for source");
  check_contains("get_noc_addr(i, dst_gen)", "get_noc_addr for destination");
  check_contains("noc_async_read", "noc_async_read");
  check_contains("noc_async_write", "noc_async_write");
  check_contains("noc_async_read_barrier()", "noc_async_read_barrier");
  check_contains("noc_async_write_barrier()", "noc_async_write_barrier");

  std::cout << "\n=== Code Preview ===" << std::endl;
  std::cout << kernel_code.substr(0, 800) << "..." << std::endl;

  std::cout << "\n========================================" << std::endl;
  if (pass) {
    std::cout << "Phase 1 CodeGen Test: PASSED" << std::endl;
    std::cout << "\nNext step: Compile kernel with TT-Metal and run on TT-Sim" << std::endl;
  } else {
    std::cout << "Phase 1 CodeGen Test: FAILED" << std::endl;
  }
  std::cout << "========================================" << std::endl;

  return pass ? 0 : 1;
}
