std::string GenerateSimpleCopyKernel(const std::string& func_name,
                                      const std::string& src_buf,
                                      const std::string& dst_buf,
                                      int num_tiles,
                                      int tile_size_bytes = 2048) {
  std::ostringstream os;

  // Header
  os << "// Simple Copy Kernel for Blackhole\n";
  os << "// Generated by TileLang CodeGenBlackhole\n";
  os << "// Function: " << func_name << "\n";
  os << "// Operation: Copy " << num_tiles << " tiles (" << tile_size_bytes
     << " bytes each)\n";
  os << "\n";

  // Include dataflow API
  os << "#include \"dataflow_api.h\"\n\n";

  // Reader kernel
  os << "// Reader Kernel - BRISC\n";
  os << "void " << func_name << "_reader(uint64_t src_dram_addr, uint32_t num_tiles) {\n";
  os << "  constexpr uint32_t cb_id = 0;\n";
  os << "  constexpr uint32_t tile_size = " << tile_size_bytes << ";\n";
  os << "  for (uint32_t i = 0; i < num_tiles; i++) {\n";
  os << "    cb_reserve_back(cb_id, 1);\n";
  os << "    uint32_t write_ptr = get_write_ptr(cb_id);\n";
  os << "    uint64_t src_addr = src_dram_addr + i * tile_size;\n";
  os << "    noc_async_read(src_addr, write_ptr, tile_size);\n";
  os << "    noc_async_read_barrier();\n";
  os << "    cb_push_back(cb_id, 1);\n";
  os << "  }\n";
  os << "}\n\n";

  // Writer kernel
  os << "// Writer Kernel - NCRISC\n";
  os << "void " << func_name << "_writer(uint64_t dst_dram_addr, uint32_t num_tiles) {\n";
  os << "  constexpr uint32_t cb_id = 0;\n";
  os << "  constexpr uint32_t tile_size = " << tile_size_bytes << ";\n";
  os << "  for (uint32_t i = 0; i < num_tiles; i++) {\n";
  os << "    cb_wait_front(cb_id, 1);\n";
  os << "    uint32_t read_ptr = get_read_ptr(cb_id);\n";
  os << "    uint64_t dst_addr = dst_dram_addr + i * tile_size;\n";
  os << "    noc_async_write(read_ptr, dst_addr, tile_size);\n";
  os << "    noc_async_write_barrier();\n";
  os << "    cb_pop_front(cb_id, 1);\n";
  os << "  }\n";
  os << "}\n\n";

  // Combined kernel (sequential execution)
  os << "// Combined Kernel - Single Core Sequential Execution\n";
  os << "void " << func_name << "(uint64_t src_dram_addr, uint64_t dst_dram_addr) {\n";
  os << "  // Execute reader then writer sequentially on same core\n";
  os << "  // CB synchronization ensures data consistency\n";
  os << "  \n";
  os << "  // Reader: DRAM -> CB\n";
  os << "  for (uint32_t i = 0; i < " << num_tiles << "; i++) {\n";
  os << "    cb_reserve_back(0, 1);\n";
  os << "    uint32_t write_ptr = get_write_ptr(0);\n";
  os << "    noc_async_read(src_dram_addr + i * " << tile_size_bytes
     << ", write_ptr, " << tile_size_bytes << ");\n";
  os << "    noc_async_read_barrier();\n";
  os << "    cb_push_back(0, 1);\n";
  os << "  }\n";
  os << "  \n";
  os << "  // Writer: CB -> DRAM\n";
  os << "  for (uint32_t i = 0; i < " << num_tiles << "; i++) {\n";
  os << "    cb_wait_front(0, 1);\n";
  os << "    uint32_t read_ptr = get_read_ptr(0);\n";
  os << "    noc_async_write(read_ptr, dst_dram_addr + i * " << tile_size_bytes
     << ", " << tile_size_bytes << ");\n";
  os << "    noc_async_write_barrier();\n";
  os << "    cb_pop_front(0, 1);\n";
  os << "  }\n";
  os << "}\n";

  return os.str();
}

std::string GenerateReaderKernel(const std::string& func_name,
                                  const std::string& src_buf,
                                  int cb_id,
                                  int num_tiles,
                                  int tile_size_bytes) {
  std::ostringstream os;

  os << "void " << func_name << "() {\n";
  os << "  // Runtime arguments\n";
  os << "  uint64_t src_dram_addr = get_arg_val<uint32_t>(0);\n";
  os << "  uint64_t src_dram_addr_hi = get_arg_val<uint32_t>(1);\n";
  os << "  src_dram_addr |= (src_dram_addr_hi << 32);\n";
  os << "  uint32_t num_tiles = get_arg_val<uint32_t>(2);\n";
  os << "\n";
  os << "  // CB configuration\n";
  os << "  constexpr uint32_t cb_id = " << cb_id << ";\n";
  os << "  constexpr uint32_t tile_size = " << tile_size_bytes << ";\n";
  os << "\n";
  os << "  // Read loop\n";
  os << "  for (uint32_t i = 0; i < num_tiles; i++) {\n";
  os << "    cb_reserve_back(cb_id, 1);\n";
  os << "    uint32_t write_ptr = get_write_ptr(cb_id);\n";
  os << "    uint64_t src_addr = src_dram_addr + i * tile_size;\n";
  os << "    noc_async_read(src_addr, write_ptr, tile_size);\n";
  os << "    noc_async_read_barrier();\n";
  os << "    cb_push_back(cb_id, 1);\n";
  os << "  }\n";
  os << "}\n";

  return os.str();
}

std::string GenerateWriterKernel(const std::string& func_name,
                                  const std::string& dst_buf,
                                  int cb_id,
                                  int num_tiles,
                                  int tile_size_bytes) {
  std::ostringstream os;

  os << "void " << func_name << "() {\n";
  os << "  // Runtime arguments\n";
  os << "  uint64_t dst_dram_addr = get_arg_val<uint32_t>(0);\n";
  os << "  uint64_t dst_dram_addr_hi = get_arg_val<uint32_t>(1);\n";
  os << "  dst_dram_addr |= (dst_dram_addr_hi << 32);\n";
  os << "  uint32_t num_tiles = get_arg_val<uint32_t>(2);\n";
  os << "\n";
  os << "  // CB configuration\n";
  os << "  constexpr uint32_t cb_id = " << cb_id << ";\n";
  os << "  constexpr uint32_t tile_size = " << tile_size_bytes << ";\n";
  os << "\n";
  os << "  // Write loop\n";
  os << "  for (uint32_t i = 0; i < num_tiles; i++) {\n";
  os << "    cb_wait_front(cb_id, 1);\n";
  os << "    uint32_t read_ptr = get_read_ptr(cb_id);\n";
  os << "    uint64_t dst_addr = dst_dram_addr + i * tile_size;\n";
  os << "    noc_async_write(read_ptr, dst_addr, tile_size);\n";
  os << "    noc_async_write_barrier();\n";
  os << "    cb_pop_front(cb_id, 1);\n";
  os << "  }\n";
  os << "}\n";

  return os.str();
}

// Generate host-side launch code
std::string GenerateHostLaunchCode(const std::string& kernel_name,
                                    int num_tiles,
                                    int tile_size_bytes,
                                    uint64_t src_addr,
                                    uint64_t dst_addr) {
  std::ostringstream os;

  os << "// Host-side launch code for " << kernel_name << "\n";
  os << "// Generated by TileLang CodeGenBlackhole\n";
  os << "\n";
  os << "#include \"tt_metal/host_api.hpp\"\n";
  os << "#include \"tt_metal/device.hpp\"\n";
  os << "#include \"tt_metal/program.hpp\"\n";
  os << "#include \"tt_metal/kernel.hpp\"\n";
  os << "#include \"tt_metal/buffer.hpp\"\n";
  os << "\n";
  os << "#include <cstdint>\n";
  os << "#include <vector>\n";
  os << "#include <iostream>\n";
  os << "\n";
  os << "using namespace tt::tt_metal;\n";
  os << "\n";
  os << "int main() {\n";
  os << "  // Device configuration\n";
  os << "  int device_id = 0;\n";
  os << "  auto device = CreateDevice(device_id);\n\n";
  os << "  // Program and core configuration\n";
  os << "  auto program = CreateProgram();\n";
  os << "  CoreCoord core = {0, 1};  // Single Tensix core\n\n";
  os << "  // Circular Buffer configuration\n";
  os << "  // CB size: 2 tiles (double buffering)\n";
  os << "  uint32_t cb_size = " << (2 * tile_size_bytes) << ";  // 2 * " << tile_size_bytes << " bytes\n";
  os << "  uint32_t cb_id = 0;\n";
  os << "  CircularBufferConfig cb_config(cb_size, {{cb_id, tt::DataFormat::Float16_b}});\n";
  os << "  auto cb = CreateCircularBuffer(program, core, cb_config);\n\n";
  os << "  // Reader kernel (BRISC)\n";
  os << "  auto reader_kernel = CreateKernel(\n";
  os << "      program,\n";
  os << "      \"" << kernel_name << "_reader.cc\",\n";
  os << "      core,\n";
  os << "      DataMovementConfig{.processor = DataMovementProcessor::RISCV_0,\n";
  os << "                         .noc = NOC::RISCV_0_default});\n\n";
  os << "  // Writer kernel (NCRISC)\n";
  os << "  auto writer_kernel = CreateKernel(\n";
  os << "      program,\n";
  os << "      \"" << kernel_name << "_writer.cc\",\n";
  os << "      core,\n";
  os << "      DataMovementConfig{.processor = DataMovementProcessor::RISCV_1,\n";
  os << "                         .noc = NOC::RISCV_1_default});\n\n";
  os << "  // Runtime arguments\n";
  os << "  uint64_t src_dram_addr = 0x" << std::hex << src_addr << std::dec << ";\n";
  os << "  uint64_t dst_dram_addr = 0x" << std::hex << dst_addr << std::dec << ";\n";
  os << "  uint32_t num_tiles = " << num_tiles << ";\n\n";
  os << "  // Set reader runtime args: src_addr_lo, src_addr_hi, num_tiles\n";
  os << "  SetRuntimeArgs(program, reader_kernel, core,\n";
  os << "                 {static_cast<uint32_t>(src_dram_addr),\n";
  os << "                  static_cast<uint32_t>(src_dram_addr >> 32),\n";
  os << "                  num_tiles});\n\n";
  os << "  // Set writer runtime args: dst_addr_lo, dst_addr_hi, num_tiles\n";
  os << "  SetRuntimeArgs(program, writer_kernel, core,\n";
  os << "                 {static_cast<uint32_t>(dst_dram_addr),\n";
  os << "                  static_cast<uint32_t>(dst_dram_addr >> 32),\n";
  os << "                  num_tiles});\n\n";
  os << "  // Execute program\n";
  os << "  std::cout << \"Launching kernel...\" << std::endl;\n";
  os << "  EnqueueProgram(device->command_queue(), program, false);\n";
  os << "  Finish(device->command_queue());\n";
  os << "  std::cout << \"Kernel execution complete\" << std::endl;\n\n";
  os << "  // Cleanup\n";
  os << "  CloseDevice(device);\n\n";
  os << "  return 0;\n";
  os << "}\n";

  return os.str();
}

// Test helper
#define CHECK_CONTAINS(code, substr) \
  do { \
    if ((code).find(substr) == std::string::npos) { \
      std::cerr << "FAIL: Generated code missing '" << substr << "'" << std::endl; \
      return false; \
    } \
  } while(0)

bool write_to_file(const std::string& filename, const std::string& content) {
  std::ofstream file(filename);
  if (!file.is_open()) return false;
  file << content;
  file.close();
  return true;
}

// Test 1: Basic FP16 copy end-to-end
bool test_e2e_fp16_copy() {
  std::cout << "\n=== E2E Test: FP16 Copy Kernel ===" << std::endl;

  // TileLang kernel parameters
  const int tile_rows = 32;
  const int tile_cols = 32;
  const int element_size = 2;  // FP16
  const int tile_size = tile_rows * tile_cols * element_size;  // 2048 bytes
  const int num_tiles = 4;
  const uint64_t src_addr = 0x10000000;
  const uint64_t dst_addr = 0x20000000;

  std::cout << "Tile configuration:" << std::endl;
  std::cout << "  Shape: " << tile_rows << "x" << tile_cols << std::endl;
  std::cout << "  Element size: " << element_size << " bytes" << std::endl;
  std::cout << "  Tile size: " << tile_size << " bytes" << std::endl;
  std::cout << "  Num tiles: " << num_tiles << std::endl;
  std::cout << "  Total data: " << (num_tiles * tile_size / 1024.0) << " KB" << std::endl;

  // Step 1: Generate device kernels
  std::cout << "\n[Step 1] Generating device kernels..." << std::endl;

  std::string reader_code = GenerateReaderKernel(
      "copy_reader", "src_dram", 0, num_tiles, tile_size);
  std::string writer_code = GenerateWriterKernel(
      "copy_writer", "dst_dram", 0, num_tiles, tile_size);
  std::string combined_code = GenerateSimpleCopyKernel(
      "copy_kernel", "src_dram", "dst_dram", num_tiles, tile_size);

  // Verify generated code
  CHECK_CONTAINS(reader_code, "noc_async_read");
  CHECK_CONTAINS(reader_code, "cb_reserve_back");
  CHECK_CONTAINS(reader_code, "cb_push_back");
  CHECK_CONTAINS(writer_code, "noc_async_write");
  CHECK_CONTAINS(writer_code, "cb_wait_front");
  CHECK_CONTAINS(writer_code, "cb_pop_front");
  CHECK_CONTAINS(combined_code, "#include \"dataflow_api.h\"");

  std::cout << "  Reader kernel: " << reader_code.size() << " bytes" << std::endl;
  std::cout << "  Writer kernel: " << writer_code.size() << " bytes" << std::endl;
  std::cout << "  Combined kernel: " << combined_code.size() << " bytes" << std::endl;

  // Step 2: Generate host launch code
  std::cout << "\n[Step 2] Generating host launch code..." << std::endl;

  std::string host_code = GenerateHostLaunchCode(
      "copy", num_tiles, tile_size, src_addr, dst_addr);

  CHECK_CONTAINS(host_code, "tt_metal/host_api.hpp");
  CHECK_CONTAINS(host_code, "CreateDevice");
  CHECK_CONTAINS(host_code, "CreateProgram");
  CHECK_CONTAINS(host_code, "CreateKernel");
  CHECK_CONTAINS(host_code, "EnqueueProgram");

  std::cout << "  Host code: " << host_code.size() << " bytes" << std::endl;

  // Step 3: Save all generated files
  std::cout << "\n[Step 3] Saving generated files..." << std::endl;

  write_to_file("e2e_copy_reader_kernel.cc", reader_code);
  write_to_file("e2e_copy_writer_kernel.cc", writer_code);
  write_to_file("e2e_copy_combined_kernel.cc", combined_code);
  write_to_file("e2e_copy_host_launch.cc", host_code);

  std::cout << "  - e2e_copy_reader_kernel.cc" << std::endl;
  std::cout << "  - e2e_copy_writer_kernel.cc" << std::endl;
  std::cout << "  - e2e_copy_combined_kernel.cc" << std::endl;
  std::cout << "  - e2e_copy_host_launch.cc" << std::endl;

  // Step 4: Print code preview
  std::cout << "\n[Step 4] Generated code preview:" << std::endl;
  std::cout << "\n--- Reader Kernel (first 15 lines) ---" << std::endl;
  std::istringstream reader_stream(reader_code);
  std::string line;
  int line_num = 0;
  while (std::getline(reader_stream, line) && line_num < 15) {
    std::cout << line << std::endl;
    line_num++;
  }

  std::cout << "\n--- Host Launch (first 15 lines) ---" << std::endl;
  std::istringstream host_stream(host_code);
  line_num = 0;
  while (std::getline(host_stream, line) && line_num < 15) {
    std::cout << line << std::endl;
    line_num++;
  }

  std::cout << "\n✓ E2E test passed: FP16 Copy kernel generation complete" << std::endl;
  return true;
}

// Test 2: Large data copy
bool test_e2e_large_copy() {
  std::cout << "\n=== E2E Test: Large Data Copy ===" << std::endl;

  const int tile_size = 2048;  // 32x32 FP16
  const int num_tiles = 128;    // 128 tiles = 256KB
  const uint64_t src_addr = 0x10000000;
  const uint64_t dst_addr = 0x30000000;

  std::cout << "Configuration: " << num_tiles << " tiles = "
            << (num_tiles * tile_size / 1024) << " KB" << std::endl;

  // Use combined kernel for compile-time num_tiles verification
  std::string combined = GenerateSimpleCopyKernel("large_copy", "src", "dst", num_tiles, tile_size);
  std::string reader = GenerateReaderKernel("large_reader", "src", 0, num_tiles, tile_size);
  std::string writer = GenerateWriterKernel("large_writer", "dst", 0, num_tiles, tile_size);
  std::string host = GenerateHostLaunchCode("large_copy", num_tiles, tile_size, src_addr, dst_addr);

  // Verify num_tiles in combined kernel (hardcoded) and host code (runtime arg)
  std::string num_tiles_str = std::to_string(num_tiles);
  CHECK_CONTAINS(combined, num_tiles_str);
  CHECK_CONTAINS(host, num_tiles_str);

  // Verify tile_size in all kernels
  std::string tile_size_str = std::to_string(tile_size);
  CHECK_CONTAINS(combined, tile_size_str);
  CHECK_CONTAINS(reader, tile_size_str);
  CHECK_CONTAINS(writer, tile_size_str);

  write_to_file("e2e_large_combined.cc", combined);
  write_to_file("e2e_large_reader.cc", reader);
  write_to_file("e2e_large_writer.cc", writer);
  write_to_file("e2e_large_host.cc", host);

  std::cout << "  Combined kernel: " << combined.size() << " bytes" << std::endl;
  std::cout << "  Host code: " << host.size() << " bytes" << std::endl;
  std::cout << "✓ Large copy test passed" << std::endl;
  return true;
}

// Test 3: Different tile sizes
bool test_e2e_tile_sizes() {
  std::cout << "\n=== E2E Test: Different Tile Sizes ===" << std::endl;

  struct TestCase {
    const char* name;
    int rows;
    int cols;
    int elem_size;
  };

  TestCase cases[] = {
      {"FP16_32x32", 32, 32, 2},    // 2048 bytes
      {"FP16_64x32", 64, 32, 2},    // 4096 bytes
      {"FP32_32x32", 32, 32, 4},    // 4096 bytes
      {"BF16_32x32", 32, 32, 2},    // 2048 bytes
  };

  for (const auto& tc : cases) {
    int tile_size = tc.rows * tc.cols * tc.elem_size;
    std::string kernel_name = std::string("copy_") + tc.name;

    std::string code = GenerateSimpleCopyKernel(
        kernel_name, "src", "dst", 8, tile_size);

    // Verify tile size appears in code
    CHECK_CONTAINS(code, std::to_string(tile_size));

    std::string filename = std::string("e2e_tile_") + tc.name + ".cc";
    write_to_file(filename, code);

    std::cout << "  " << tc.name << ": " << tile_size << " bytes/tile - OK" << std::endl;
  }

  std::cout << "✓ Tile size test passed" << std::endl;
  return true;
}

